public class Main {
    public static void main(String[] args) {
        //different between println vs print
        //if we use println
        System.out.println("Hello");
        System.out.println(" World!");
        //we gonna see println print in new line
        //if we use print
        System.out.print("Hello");
        System.out.print(" World!");
        //we gonna see it is print in same line

    }
}